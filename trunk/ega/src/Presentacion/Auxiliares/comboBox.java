package Presentacion.Auxiliares;

import javax.swing.JComboBox;
import javax.swing.JTable;

/**
 *
 * @author Nicolas
 */
public class comboBox {
    public JComboBox combo;
    public JTable table;
    public String strFileName;
    public String strFirstLetter;
    
    public comboBox(){}
    
    public comboBox(JComboBox combo, String strFileName, String strFirstLetter, JTable table){
        this.strFileName = strFileName;
        this.table = table;
        this.combo = combo;
        this.strFirstLetter = strFirstLetter;
    }
}
